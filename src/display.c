//----------------------------------------------------------------------------------------------------------------------

void displayTitle()
{
    clearArea(0, 0, 80, 1, 1);
    write(1, 0, 1, "Ed (v1.0)");
}

//----------------------------------------------------------------------------------------------------------------------

void displayStatusBar()
{
    clearArea(0, 31, 80, 1, 1);
    write(1, 31, 1, "Ln 0001  Col 0001");
}

//----------------------------------------------------------------------------------------------------------------------


void displayScreen()
{
    intrinsic_halt();
    clearArea(0, 1, 80, 30, 0);
    displayTitle();
    displayStatusBar();
}

